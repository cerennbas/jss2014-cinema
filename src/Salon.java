
public class Salon {
    private int rows;
    private int cols;
    private String name;
    
    
    
    public Salon(int rows, int cols, String name) {
		super();
		this.rows = rows;
		this.cols = cols;
		this.name = name;
	}

	public int getRows() {
		return rows;
	}
	
	public int getCols() {
		return cols;
	}
	
	public String getName() {
		return name;
	}
    
}
